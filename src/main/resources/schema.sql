DROP TABLE IF EXISTS CONGESTION_TAX_TABLE;
DROP TABLE IF EXISTS VEHICLE_MOVEMENT;
DROP TABLE IF EXISTS CITY;
CREATE TABLE CITY
(
    id        NUMERIC(20) NOT NULL,
    city_name VARCHAR(512) NOT NULL,
    city_code VARCHAR(10)  NOT NULL,
    PRIMARY KEY (id)
);

CREATE TABLE CONGESTION_TAX_TABLE
(
    id         NUMERIC(20),
    city_id    NUMERIC(20),
    cost       NUMERIC(3),
    start_time TIME,
    end_time   TIME,
    PRIMARY KEY (id),
    FOREIGN KEY (city_id) REFERENCES CITY (id)

);

CREATE TABLE VEHICLE_MOVEMENT
(
    id         NUMERIC(20),
    REG_NUM    VARCHAR(10),
    city_id    NUMERIC(20),
    VEHICLE_TYPE VARCHAR (100),
    TIME_RECORDED TIMESTAMP,
    PRIMARY KEY (id),
    FOREIGN KEY (city_id) REFERENCES CITY (id)
);

